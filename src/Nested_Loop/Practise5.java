package Nested_Loop;

public class Practise5 {

	public static void main(String[] args) {
		for (int i=1; i<=5; i++) {
			for (int d=1; d<=5; d++) {
				if (i==d) {
					
				
					System.out.print(" % ");
				
}
				else 
				{
					System.out.print("  ");
				}
			
		}
			System.out.println();
		}
			
	}
}

				
			
		